package cursoJava2024;

public class If {
	public static void main(String[] args) {
		// Conhecendo a condicional IF:

		int idade = 200;

		// verificando o nome exato:

		if (idade > 200) {
			System.out.println("O valor é maior que 200!");

		} else if (idade < 200) {
			System.out.println("O valor é menor que 200 :(");

		} else if (idade == 200) {
			System.out.println("O valor é igual a 200 :D");

		}

	}

}
